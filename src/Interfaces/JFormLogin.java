    private void btnDesbloquearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesbloquearActionPerformed
        JFormInterfaz inter = new JFormInterfaz(txtUsuario.getText());
        if (ExisteUsuario() == true) {
            Usuariotrans = txtUsuario.getText();
            inter.setVisible(true);
            this.dispose();
        } else {
            if ("admin".equals(txtUsuario.getText()) && !"admin".equals(txtContra.getText())) {
                JOptionPane.showMessageDialog(null, "Contraseña incorrecta");
            } else {
                JOptionPane.showMessageDialog(null, "Usuario incorrecto");
            }
        }

    }//GEN-LAST:event_btnDesbloquearActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (ExisteUsuario() == true) {
            JOptionPane.showMessageDialog(null, "Usuario ya existe");
        } else {
            if ("".equals(txtUsuario.getText())) {
                JOptionPane.showMessageDialog(null, "Ingrese usuario");
            } else if ("".equals(txtContra.getText())) {
                JOptionPane.showMessageDialog(null, "Ingrese contraseña");
            } else {
                agregar();
                JOptionPane.showMessageDialog(null, "Usuario creado");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    boolean ExisteUsuario() {
        boolean bexis = false;
        Connection con = Conexion.getConexion();
        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM USUARIO WHERE USUARIO='" + txtUsuario.getText() + "' AND CONTRA='" + txtContra.getText() + "'");
            while (rs.next()) {
                bexis = true;
            }
            rs.close();
            stmt.close();
            return bexis;
        } catch (Exception e) {
            e.printStackTrace();
        }
        return bexis;
    }

    public boolean agregar() {
        boolean bsave = false;
        Connection con = Conexion.getConexion();
        try {
            String sql = "INSERT INTO USUARIO VALUES('" + txtUsuario.getText() + "' ,'" + txtContra.getText() + "') \n"
                    + "DECLARE @LASTID AS INT \n"
                    + "SET @LASTID=(SELECT TOP 1 ISNULL(IDCARTERA,1)IDCARTERA FROM CLIENTE ORDER BY IDCARTERA DESC)\n"
                    + "INSERT INTO CLIENTE VALUES(@LASTID+1,0.00,'BTC','" + txtUsuario.getText() + "')\n"
                    + "INSERT INTO CLIENTE VALUES(@LASTID+1,0.00,'OASI','" + txtUsuario.getText() + "')\n"
                    + "INSERT INTO CLIENTE VALUES(@LASTID+1,0.00,'USDT','" + txtUsuario.getText() + "')";

            PreparedStatement stmt = con.prepareStatement(sql);
            stmt.executeUpdate();
            stmt.close();
            bsave = true;
        } catch (SQLException ex) {
            Logger.getLogger(Conexion.class.getName()).log(Level.SEVERE, null, ex);
        }
        return bsave;
    }

